class  c1:
	x = 10  #  static  variable
	def  m1(self):
		self . x = 20
a = c1()  #  Empty  object
a . m1() #   Adds  variable  'x'  to  object  'a'  with  value   20
print(c1 . x) # 10
print(a . x) # 20


