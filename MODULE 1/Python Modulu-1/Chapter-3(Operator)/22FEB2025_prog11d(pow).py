# pow()  function  demo  program
from  builtins  import  pow
print(pow(10 , -2))  #  10 ^ -2 = 0.01
print(pow(4 , pow(3 , 2)))  #  4 ^ 3 ^ 2 = 4 ^ 9
import  builtins
print(builtins . pow(2 , 3))  #   2 ^ 3
print(builtins . pow(-2 , -3)) #  -2 ^ -3



