#  ++  and  --  operators  demo  program
a = 25
print(++a)  #  +(+a) = +a = 25
#print(a++) #  (a+)+  = a+ = 25+  throws  error
print(a++1)  # a + (+1)  =  25 + 1 = 26
print(--a) #   -(-a) = +a = 25
#print(a--) #  (a-)-  = a+  = 25+  throws  error
print(a--1) #  a - (-1)  = a + 1 = 26
print(-a) #  -25
print(+-a) #  +(-a) = -a = -25
print(-+a) #  -(+a) = -a = -25



