'''
Write  a  program  to  print  first  20  odd  numbers  with  while  loop

1) What  are  the  first  20  odd  numbers  ?  --->  1 , 3 , 5 , ....  39

2) In  general, print  what ?  --->  2 * i  - 1  where  i  varies  from  1  to  20
'''
print('First  20  odd  numbers')
i = 1
while i <= 20:
	print(2 * i - 1)
	i += 1
