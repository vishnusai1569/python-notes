'''
(Home  work)
Write  a  program  to  print  first  20  even  numbers  with  while  loop

1) What  are  the  first  20  even  numbers ?  --->  2 , 4 , 6 , 8 , ....   40

2) What  is  printed  in  general ?  --->  2 * i  where  i  varies  from  1  to  20

3) Use  while  loop
'''
print('First  20  even   numbers')
i = 1
while  i <= 20:
	print(2 * i)
	i += 1
print('Bye')
