# Find  outputs  (Home  work)
for   x  in  [10,20,15,18]:  #  How  to  print  each  element  of  list  [10 , 20 , 15 , 18]  using  for  loop
	print(x)  #  10  <next  line>  20  <next  line>  15  <next  line>  18  <next  line>
print()
for  ch  in  'Hyd':  #  How  to  print  each  character  of   string  'Hyd'  using  for  loop
	print(ch)  #   H   <next  line>  y  <next  line>  d  <next  line>
print()
for  x  in   range(5):  # How  to  print  each  element  of   range(5) using  for  loop
	print(x)  #  0  <next  line>  1  <next  line>  2  <next  line>  3  <next  line>  4    <next  line>
