'''
Write  a  program  to  print  natural  numbers  1 , 2 , 3 .... n  and   ask  user  to  input  value  of  'n'

In  general,  print  what ?  --->  Print  'i'  where  'i'  varies  from  1  to  n

Hint:  Use  for  loop
'''
n = int(input('Enter value of n : '))
i = 1
print('Natural  Numbers')
while  i <= n:
		print(F'{i}')
		i +=1
