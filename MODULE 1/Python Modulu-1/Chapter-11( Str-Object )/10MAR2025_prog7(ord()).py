# ord()  function  demo  program
print(ord('A'))  #  Unicode  value  of  'A'  i.e.  65
print(ord('Z'))  #  90
print(ord('a'))  #  97
print(ord('z')) #  122
print(ord('0')) #  48
print(ord('9')) #  57
print(ord('$')) #  36
print(ord(' ')) #  32



