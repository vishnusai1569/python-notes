# center()  method  demo  program
a = 'Rama Rao'
print(a . center(20))   #  <6  space>Rama Rao<6  spaces>
print(a . center(20 , '*')) #   ******Rama Rao******
print(a . center(11 , '$'))  #  $$Rama Rao$
print(a . center(5))  #   Rama  Rao  and  smaller  width  5  is  ignored


# center()  method  returns  string  at  the  center  in  the  specified  width
