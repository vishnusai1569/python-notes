 #Find  outputs (Home  work)
power = lambda  a = 3.5 , b = 2  :  a ** b
print(power(2 , 3)) #  'a'  is  2 , 'b'  is  3 , result  is  2^3 =  8
print(power(4.5 , 4))  #  'a'  is  4.5 , 'b'  is   4 , result  is  4.5^4  = 410.0625
print(power()) #  'a'  is   default  value  3.5  , 'b'  is  default  value  2 , result  is  3.5^2 = 12.25
print(power(9))  #  'a'  is  9  , 'b'  is  default  value  2 , result  is  9^2 =81
